# The process of converting one type of data to another type of data is called
#  as typecasting.
#  Syntax ==> datatype(variable/value)
#  The typecasting functions are
#  1. float(variable or value)
#  2. str(variable or value)
#  3. list(variable or value).

#converting int to float
a=10
print(float(a))   #a=10 output==> 10.0

print(float(20))  #value=20 output==>20.0

#converting int to string
print(str(a))     #a=10 output==>'10'

#converting int to list
a=10,20
print(list(a))  #a=10,20 output==> [10,20]
# note single integer cannot be converted to list a=10 to list is not possible
# note group of integer values can ve converted to list a=10,20,30 to list is possible

#converting int to tuple
a=10,20

#converting string to list
a='qspiders'
print(list(a))























